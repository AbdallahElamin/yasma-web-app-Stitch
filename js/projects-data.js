// ===================================
// HELPER FUNCTIONS
// ===================================

/**
 * Get all projects
 */
function getAllProjects() {
    return PROJECTS_DATA;
}

/**
 * Get a specific project by ID
 * @param {string} projectId - Project ID (e.g., 'Project01')
 * @returns {object|null} Project object or null if not found
 */
function getProjectById(projectId) {
    return PROJECTS_DATA.find(project => project.id === projectId) || null;
}

/**
 * Get projects filtered by category/badge
 * @param {string} category - Category to filter by (e.g., 'Commercial', 'Residential', 'Fuel Station')
 * @returns {array} Array of projects matching the category
 */
function getProjectsByCategory(category) {
    if (category === 'All') {
        return PROJECTS_DATA;
    }
    return PROJECTS_DATA.filter(project => project.badge === category);
}

/**
 * Get featured projects (first N projects)
 * @param {number} count - Number of projects to return (default: 3)
 * @returns {array} Array of featured projects
 */
function getFeaturedProjects(count = 3) {
    return PROJECTS_DATA.slice(0, count);
}

/**
 * Get unique project categories
 * @returns {array} Array of unique categories
 */
function getProjectCategories() {
    const categories = [...new Set(PROJECTS_DATA.map(project => project.badge))];
    return ['All', ...categories];
}
